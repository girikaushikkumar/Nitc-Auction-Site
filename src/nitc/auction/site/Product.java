    public void SetCategory()
    {
        
        try {
            Connection con=DBConnection.connectDB();
            PreparedStatement st=con.prepareStatement("select * from category");
            ResultSet rss = (java.sql.ResultSet) st.executeQuery();
             while(rss.next()){
                try {
                    cat.addItem(rss.getString("catName"));
                } catch (SQLException ex) {
                    Logger.getLogger(ManageSubCategories.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
            con.close();
        } catch (SQLException ex) {
            Logger.getLogger(ManageSubCategories.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    public void SetSubCategory()
    {
        try {
            Connection con=DBConnection.connectDB();
            PreparedStatement st=con.prepareStatement("select * from subcategory");
            ResultSet rss = (java.sql.ResultSet) st.executeQuery();
            
            subcat.removeAllItems();
             while(rss.next()){
                try {
                   
                    if(cat.getSelectedItem().toString().equals(rss.getString("category_type")))
                    {
                        subcat.addItem(rss.getString("subcategory_name"));
                    }
                             
                } catch (SQLException ex) {
                    Logger.getLogger(ManageSubCategories.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
            con.close();
        } catch (SQLException ex) {
            Logger.getLogger(ManageSubCategories.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

