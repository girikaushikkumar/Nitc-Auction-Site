    private void AddProduct(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_AddProduct
         String sql="insert into product values(?,?,?,?,?,?,?,?,?,?,?,?)";
         if(pdtName.getText().isEmpty() || pdtPrice.getText().isEmpty() )
              JOptionPane.showMessageDialog(this,"Missing Information");

        try {
            Connection con=DBConnection.connectDB();
            String query = "SELECT COUNT(*) FROM product";
            Statement stmt = (Statement) con.createStatement();
            ResultSet rs = stmt.executeQuery(query);
            rs.next();
            int count = rs.getInt(1);
            
            
            PreparedStatement st=(PreparedStatement)con.prepareStatement(sql);

            String pdtId = pdtName.getText()+" "+count;
            st.setString(1,pdtId);
            st.setString(2,pdtName.getText());
            st.setString(3,cat.getSelectedItem().toString());
            st.setString(4, subcat.getSelectedItem().toString());
            st.setString(5,pdtBrand.getText());
            st.setString(6,pdtInfo.getText());
            
            try {
                  int Value = Integer.parseInt(pdtPrice.getText());
                  st.setInt(7,Value);
               } catch (NumberFormatException e) {
                   JOptionPane.showMessageDialog(this, "Price should be number only");
                }
            

            Timestamp curdate = new Timestamp(new java.util.Date().getTime());
            st.setTimestamp(8, curdate);
            
            String selectdate = ((JTextField)pdtDate.getDateEditor().getUiComponent()).getText();
//            System.out.println(pdtDate.getDate());
            
             try {
                    LocalTime.parse(pdtTime.getText());
                    String endtime = selectdate + "-" + pdtTime.getText();
                    st.setString(9, endtime);
                } catch (DateTimeParseException | NullPointerException e) {
                    JOptionPane.showMessageDialog(this, "Use Proper time format");
                    pdtTime.setText("HH:MM");
                    e.printStackTrace();
                }
            
            
//            System.out.println(endTime.getValue().toString());
           
            
           

            
            st.setBytes(10, person_img);

//            Login login=new Login();
//            Student student = new Student();
//            String sellerId=student.userId;
            System.out.println(userId);
            st.setString(11,userId);
            st.setString(12, "pending");


            st.executeUpdate();

            JOptionPane.showMessageDialog(this,"Added Successfully");
            SellerDashboard sd = new SellerDashboard();
            sd.suserId=userId;
            sd.setVisible(true);
            this.dispose();
            con.close();
            
         
            
        } catch (Exception e) {
            e.printStackTrace();
            
        }
    }//GEN-LAST:event_AddProduct

